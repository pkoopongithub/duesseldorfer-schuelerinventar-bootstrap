<H1>Düsseldofer Schülerinventar:</H1>
Das Duesseldorfer Schuelerinventar ist ein offenes, gueltiges, genaues und unabhaengiges Persoenlichkeitsinventar fuer Schueler der Uebergangsklassen (valide, reliabel, objektiv). Das Duesseldorfer Schuelerinventar ist entwicklungsoffen. Ich freue mich ueber wissenschaftliche Reanalysen und weitere Entwicklungsteams. Gerne stelle ich zu wissenschaftlichen Zwecken die SPSS-Dateien zur Verfuegung. Das Duesseldorfer Schuelerinventar Duesk wurde von Praktikern fuer Praktiker entwickelt und ist aus der Berufsorientierung fuer Schuelerinnen und Schueler der Jahrgangsstufen 8 aus Duesseldorfer Haupt- und Foerderschulen hervorgegangen. In der Entwicklung von Duesk stand von Beginn an das Ziel im Vordergrund, zu den bekannten sonder- und foerderpaedagogischen Messinstrumenten eine Alternative zu finden, die berufspaedagogisch ausgerichtet ist, weder einen klinischen noch einen foerderpaedagogischen Ansatz verfolgt und in einem kostenguenstigen und zeitlich vertretbaren Rahmen die arbeitspaedagogisch relevanten informellen Kompetenzen in einer Selbst- und Fremdeinschaetzung messen kann. In den letzten Jahren sind verschiedene Persoenlichkeitsprofile am Testmarkt erschienen, die keinen klinischen Hintergrund haben und sich konsequent am arbeitspsychologischen oder berufspaedagogischen Bedarf orientieren. Zu nennen sind hier stellvertretend fuer viele andere etwa das Bochumer-, das Freiburger- oder das Trierer Inventar. Viele Inventare, wie das Bochumer Inventar beziehen sich auf Eichstrichproben berufstaetiger Personen. Andere, wie das Trierer- oder Freiburger Inventar beziehen Schueler der uebergangsklassen ein, wenden sich jedoch an Schueler der 10. Klasse. Das Duesseldorfer Schuelerinventar wurde bewusst auf den Berufswahlpass fuer die Sekundarstufe I (Version 2009) bezogen und ergaenzt dessen Selbsteinschaetzungs- und Fremdeinschaetzungsbogen optimal. Im Handlungsfeld Uebergang Schule Beruf wird der Berufswahlpass und die dort enthaltene Selbst- und Fremdeinschaetzung von vielen Schuelern, Eltern und Lehrern regelmaessig genutzt und ist den beteiligten Akteuren vertraut. 

