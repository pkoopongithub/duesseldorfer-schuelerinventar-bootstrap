		  <h1>Was ist D&Uuml;SK?</h1>
          <br>Seit 2012 dient das quelloffene D&uuml;sseldorfer Sch&uuml;lerinventar als Lernumgebung f&uuml;r Studenten der Sozialwissenschaften und Auszubildende der Markt- und Sozialforschung, der Anwendungsentwicklung und der Daten- und Prozessanalyse.<br>
		  <br>Das D&uuml;sseldorfer Sch&uuml;lerinventar ist ein <strong>offenes, g&uuml;ltiges, genaues und unabh&auml;ngiges Pers&ouml;nlichkeitsinventar</strong> f&uuml;r Sch&uuml;ler der &Uuml;bergangsklassen (valide, reliabel, objektiv). Das D&uuml;sseldorfer Sch&uuml;lerinventar ist 
		  <strong>entwicklungsoffen.</strong> Wenn Sie an einem Open-Source-Projekt mitwirken, kennen Sie das intensive, pers&ouml;nliche Engagement, das zum Gelingen beitr&auml;gt. Viele Nutzer wissen und sch&auml;tzen das. Aus der eigenen Kraft der Entwickler gelingt aber nicht alles, zumal vielfach nur das pure Interesse am Thema oder der Umgang mit Technologien die Basis abgibt. Nicht jeder Nutzer verf&uuml;gt jedoch &uuml;ber die F&auml;higkeiten und die Zeit, um ein Projekt zu unterst&uuml;tzen. Verbales Feedback in Form von Fehlermeldungen ist sehr hilfreich. F&uuml;r andere kann eine materielle Unterst&uuml;tzung die einzige M&ouml;glichkeit sein, sich an Open Source zu beteiligen. Gerne spreche ich mit Ihnen &uuml;ber Ihre Art der Unterst&uuml;tzung.<br><br>Ich freue mich &uuml;ber wissenschaftliche Reanalysen und weitere Entwicklungsteams. Gerne stelle ich zu wissenschaftlichen Zwecken die SPSS- 
		  und R-Dateien zur Verf&uuml;gung. Zur Weiterentwicklung stelle ich 
		  gerne die Quellcodes der Programme zur Verfügung (<span class="d2edcug0 hpfvmrgz qv66sw1b c1et5uql rrkovp55 a8c37x1j keod5gw0 nxhoafnm aigsh9s9 d3f4x2em fe6kdd0r mau55g9w c8b282yb iv3no6db jq4qci2q a3bd9o3v knj5qynh oo9gr5id hzawbc8m" dir="auto">PHP, 
		  MySQL, Xcode, Android Studio, Xamarin, Lazarus</span>).<br><br>Das D&uuml;sseldorfer Sch&uuml;lerinventar D&uuml;sk wurde von Praktikern f&uuml;r Praktiker entwickelt und ist aus der Berufsorientierung f&uuml;r Sch&uuml;lerinnen und Sch&uuml;ler der Jahrgangsstufen 8 aus D&uuml;sseldorfer Haupt- und F&ouml;rderschulen hervorgegangen. In der Entwicklung von D&uuml;sk stand von Beginn an das Ziel im Vordergrund, zu den bekannten sonder- und f&ouml;rderp&auml;dagogischen Messinstrumenten eine Alternative zu finden, die berufsp&auml;dagogisch ausgerichtet ist, weder einen klinischen noch einen f&ouml;rderp&auml;dagogischen Ansatz verfolgt und in einem kosteng&uuml;nstigen und zeitlich vertretbaren Rahmen die arbeitsp&auml;dagogisch relevanten informellen Kompetenzen in einer Selbst- und Fremdeinsch&auml;tzung messen kann. In den letzten Jahren sind verschiedene Pers&ouml;nlichkeitsprofile am Testmarkt erschienen, die keinen klinischen Hintergrund haben und sich konseq&uuml;nt am arbeitspsychologischen oder berufsp&auml;dagogischen Bedarf orientieren. Zu nennen sind hier stellvertretend f&uuml;r viele andere etwa das Bochumer-, das Freiburger- oder das Trierer Inventar. Viele Inventare, wie das Bochumer Inventar beziehen sich auf Eichstrichproben berufst&auml;tiger Personen. Andere, wie das Trierer- oder Freiburger Inventar beziehen Sch&uuml;ler der &uuml;bergangsklassen ein, wenden sich jedoch an Sch&uuml;ler der 10. Klasse. Das D&uuml;sseldorfer Sch&uuml;lerinventar wurde bewusst auf den Berufswahlpass f&uuml;r die Sekundarstufe I (Version 2009) bezogen und erg&auml;nzt dessen Selbsteinsch&auml;tzungs- und Fremdeinsch&auml;tzungsbogen optimal. Im Handlungsfeld &uuml;bergang Schule Beruf wird der Berufswahlpass und die dort enthaltene Selbst- und Fremdeinsch&auml;tzung von vielen Sch&uuml;lern, Eltern und Lehrern regelm&auml;ssig genutzt und ist den beteiligten Akteuren vertraut.

