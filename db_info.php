		  Lazarus)</span>. Das bietet sonst kein Lehrbuch. Innovativ ist, etwas Einfaches erstmalig einfach zu denken.
</p>
		  <p>
		  <strong>Ich biete Schulung zu individueller Erstellung und Auswertung zum D&uuml;sseldorfer Sch&uuml;lerinventar D&Uuml;SK</strong>
		  (Erstgespr&auml;ch, Beratungsgespr&auml;ch, Rohdatenerhebung, Auswertung, 
		  Auswertungsgespr&auml;ch, Fahrtkosten und Aufwand nach Vereinbarung). Ich biete <strong>individuelle Erstellung und Auswertung</strong> zum D&uuml;sseldorfer Sch&uuml;lerinventar D&Uuml;SK und zum Bochumer Inventar zur berufsbezogenen Pers&ouml;nlichkeitsbeschreibung BIP (Erstgespr&auml;ch, Beratungsgespr&auml;ch, Rohdatenerhebung, Auswertung, Auswertungsgespr&auml;ch, Fahrtkosten und Aufwand nach Vereinbarung)</p>
		  <p>
		  <br>
		  Big-Data-Analysen f&uuml;r die Analyse von Pers&ouml;nlichkeit, Einstellungen 
		  und Verhaltensweisen sind sehr fragw&uuml;rdig. Ich setze dagegen auf 
		  offenes Testdesign, Datenschutz, klassische Testtheorie und 
		  kommunikative Validierung der Empfehlungen im herrschaftsfreien 
		  Beratungsgespr&auml;ch. </p>
		  <p>
		  Persönlichkeitsinventare sind Selbstbeschreibungen eigener Potentiale 
		  und keine (klinischen) Mängelanalysen. Sie gehören zum 
		  herrschaftsfreien Beratungsgespräch zusammen mit Fremdeinschätzung, 
		  Lebenslauf und Biographie. Das setzt Vertrauen, gegenseitigen Respekt 
		  und herrschaftsfreie Rahmenbedingungen voraus.</p>
