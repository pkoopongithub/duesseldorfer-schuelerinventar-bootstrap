<h2>Kurzanleitung:</h2>
<p>Diese Kurzanleitung beschreibt die Bedienung dieser Version. Die Nutzung wird nicht erklärt. 
<strong>Für die Nutzung ist</strong> 
die Kenntnis der klassischen Testtheorie, <strong>die Kenntnis des Handbuches</strong> und eine 
Abschlussgespräch mit einem Berater <strong>erforderlich</strong>. Dazu dient diese Seite nicht. 
Die Seite dient dazu die Quellcodes und Rohdaten zur Verfügung zu stellen um 
Nutzern die Möglichkeit zum Nachprogrammieren und Weiterentwickeln zu geben. 
<strong>Zur 
Nutzung sind allein lokale Installationen im lokalen Netzwerk gedacht</strong>.</p>



<div class="content">
  <img src="http://duesk.org/anmelden.JPG" alt="Bear" style="width:100%">
  <h3>Anmelden:</h3>
  <p>Zum Anmelden benutzen Sie den Gastzugang, legen einen neuen Benutzer durch Ausfüllen der Felder an oder nutzen Ihre Benutzerkennung.</p>
</div>

<div class="content">
  <img src="http://duesk.org/menue.JPG" alt="Bear" style="width:100%">
  <h3>Menü:</h3>
  <p>Aus dem Menü können Sie das Einfügen eines neuen Datensatzes, das Bearbeiten eines Datensatzes und das Abmelden wählen.</p>
</div>

<div class="content">
  <img src="http://duesk.org/eingabe.JPG" alt="Bear" style="width:100%">
  <h3>Eingabe:</h3>
  <p>Beim Einfügen eines neuen Datensatzes vergeben Sie einen Namen für den Datensatz und legen eine Gruppe an oder fügen zu einer bestehenden Gruppe hinzu. Für die Selbsteinschätzung und Fremdeinschätzung erfassen Sie die Daten aus den Rohdatenblättern für jeses Item.</p>
</div>
  
<div class="content">
  <img src="http://duesk.org/bearbeiten.JPG" alt="Bear" style="width:100%">
  <h3>Bearbeiten:</h3>
  <p>Zum Bearbeiten wählen Sie den zu bearbeitenden Datensatz und können, löschen, ändern oder das Profil erstellen.</p>
</div>

<div class="content">
  <img src="http://duesk.org/profilansicht.JPG" alt="Bear" style="width:100%">
  <h3>Profil:</h3>
  <p>Unter Bearbeiten wählen Sie die Erstellung des Profils nach den Normen HS oder FS. 
  Die Interpretation des Profils setzt die Kenntnis der klassischen Testheorie 
  und die Kenntnis des Handbuches voraus. Das Profil ist eine Zusammenfassung 
  der selbstbestimmten und herrschaftsfreien Selbsteinschätzung und keine 
  fremdbestimmte Mängelanalyse.</p>
</div>

